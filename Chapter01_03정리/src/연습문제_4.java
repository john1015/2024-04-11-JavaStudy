//4. char형 변수 ch가 숫자('0' ~ '9')일 때 true인 조건식 작성

public class 연습문제_4 {

	public static void main(String[] args) {
		
		char ch ='0';
		if(ch=='0'||ch=='1'||ch=='2'||ch=='3'||ch=='4'||ch=='5'||ch=='6'||ch=='7'||ch=='8'||ch=='9')
			System.out.println(true);
		else System.out.println(false);
	}

}
